namespace VinylStoreMVC.Models
{
    /// <summary>
    /// Представляет модель данных для отображения информации об ошибке в приложении.
    /// Используется для передачи данных об ошибках на страницу Error.
    /// </summary>
    public class ErrorViewModel
    {
        /// <summary>
        /// Задаёт уникальный идентификатор запроса, связанного с ошибкой.
        /// Используется для диагностики и поиска соответствующих записей в логах.
        /// </summary>
        /// <value>Строковый идентификатор запроса или null, если идентификатор недоступен.</value>
        public string? RequestId { get; set; }

        /// <summary>
        /// Задаёт флаг, указывающий, следует ли отображать идентификатор запроса на странице ошибки.
        /// Определяется наличием непустого значения в свойстве <see cref="RequestId"/>.
        /// </summary>
        /// <value>true, если идентификатор запроса существует и не пуст; в противном случае false.</value>
        public bool ShowRequestId => !string.IsNullOrEmpty(RequestId);
    }
}
